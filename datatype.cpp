#include<iostream>
using namespace std;
int main(){
    int a;
    a=12;
    cout<<"Size of a is "<< sizeof(a)<<endl;
    float b;
    cout<<"Size of b is "<< sizeof(b)<<endl;
    char c;
    cout<<"Size of c is "<< sizeof(c)<<endl;
    bool d;
    cout<<"Size of d is "<< sizeof(d)<<endl;
    short int si;
    long int li;
    cout<<"Size of si is "<< sizeof(si)<<endl;
    cout<<"Size of li is "<< sizeof(li)<<endl;

    return 0;

}